/**
 * Create a new bot
 */
export async function createBot(bot: EmailBot): Promise<EmailBot> {
    const supabase = createAdminClient();

    const dbRow = mapBotToRow(bot);
    // Remove ID if it's auto-generated by DB? 
    // But our ID is uuid, so we can pass it if we generated it in code.
    // The previous implementation generated ID in action. Let's keep that.

    const { data, error } = await supabase
        .from('bots')
        .insert(dbRow)
        .select()
        .single();

    if (error) {
        console.error('Error creating bot:', error);
        throw new Error('Failed to create bot');
    }

    return mapRowToBot(data);
}

/**
 * Update a bot
 */
export async function updateBot(
    botId: string,
    userId: string,
    updates: Partial<EmailBot>
): Promise<EmailBot> {
    const supabase = createAdminClient();

    // Map updates to DB columns
    const dbUpdates: any = {};
    if (updates.name) dbUpdates.name = updates.name;
    if (updates.description) dbUpdates.description = updates.description;
    if (updates.enabled !== undefined) dbUpdates.enabled = updates.enabled;
    if (updates.isPremium !== undefined) dbUpdates.is_premium = updates.isPremium;
    if (updates.trigger) dbUpdates.trigger = updates.trigger;
    if (updates.conditions) dbUpdates.conditions = updates.conditions;
    if (updates.actions) dbUpdates.actions = updates.actions;
    if (updates.safety) dbUpdates.safety = updates.safety;
    if (updates.stats) dbUpdates.stats = updates.stats;
    if (updates.acceptedTermsAt) dbUpdates.accepted_terms_at = updates.acceptedTermsAt;
    if (updates.acceptedTermsVersion) dbUpdates.accepted_terms_version = updates.acceptedTermsVersion;

    dbUpdates.updated_at = new Date().toISOString();

    const { data, error } = await supabase
        .from('bots')
        .update(dbUpdates)
        .eq('id', botId)
        .eq('user_id', userId)
        .select()
        .single();

    if (error) {
        console.error('Error updating bot:', error);
        throw new Error('Failed to update bot');
    }

    return mapRowToBot(data);
}

