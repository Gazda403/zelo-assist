/**
 * Count successful sends today for a bot
 */
export async function countSendsToday(botId: string): Promise<number> {
    // Implementation would need a date filter query
    // For now, simpler to fetch recent logs or trust the Stats field
    const supabase = createAdminClient();
    const today = new Date().toISOString().split('T')[0];

    const { count, error } = await supabase
        .from('bot_execution_logs')
        .select('*', { count: 'exact', head: true })
        .eq('bot_id', botId)
        .eq('status', 'success')
        .gte('triggered_at', `${today}T00:00:00.000Z`);

    if (error) return 0;
    return count || 0;
}

/**
 * Get last send time to a specific email for a bot
 */
export async function getLastSentTo(
    botId: string,
    email: string
): Promise<Date | null> {
    const supabase = createAdminClient();

    // This is tricky because senderEmail is in metadata jsonb
    // Supabase allows filtering on jsonb columns
    const { data, error } = await supabase
        .from('bot_execution_logs')
        .select('triggered_at')
        .eq('bot_id', botId)
        .eq('status', 'success')
        .contains('metadata', { senderEmail: email })
        .order('triggered_at', { ascending: false })
        .limit(1)
        .single();

    if (error || !data) return null;

    return new Date(data.triggered_at);
}

