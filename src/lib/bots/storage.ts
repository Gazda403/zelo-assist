/**
 * Get bot statistics
 * (For Supabase, simpler to just read it from the bot record itself, checking `stats` column)
 */
export async function getBotStats(botId: string): Promise<BotStats | null> {
    // We can just fetch the bot and return its stats
    // The previous implementation had a separate file. 
    // In our DB schema, we put 'stats' as a JSONB column on the 'bots' table.
    // However, if we want to run atomic updates (increments), we might need an RPC or separate table.
    // For now, let's just query the bot.

    const supabase = createAdminClient();
    const { data, error } = await supabase
        .from('bots')
        .select('stats, updated_at') // we might want updated_at as lastExecuted? No, stats has it.
        .eq('id', botId)
        .single();

    if (error || !data) return null;

    // Ensure stats object has defaults
    const stats = data.stats || {};
    return {
        totalExecutions: stats.totalExecutions || 0,
        lastExecutedAt: stats.lastExecutedAt ? new Date(stats.lastExecutedAt) : undefined,
        successCount: stats.successCount || 0,
        failureCount: stats.failureCount || 0,
        emailsSent: stats.emailsSent || 0,
        draftsCreated: stats.draftsCreated || 0,
        lastUsedTemplate: stats.lastUsedTemplate,
    };
}

/**
 * Update bot stats incrementally
 */
export async function updateBotStats(
    botId: string,
    result: {
        success: boolean;
        emailsSent?: number;
        draftsCreated?: number;
        lastUsedTemplate?: string;
    }
): Promise<void> {
    const currentStats = await getBotStats(botId) || {
        totalExecutions: 0,
        successCount: 0,
        failureCount: 0,
        emailsSent: 0,
        draftsCreated: 0,
    };

    const newStats: BotStats = {
        ...currentStats,
        totalExecutions: (currentStats.totalExecutions || 0) + 1,
        lastExecutedAt: new Date(),
        successCount: (currentStats.successCount || 0) + (result.success ? 1 : 0),
        failureCount: (currentStats.failureCount || 0) + (result.success ? 0 : 1),
        emailsSent: (currentStats.emailsSent || 0) + (result.emailsSent || 0),
        draftsCreated: (currentStats.draftsCreated || 0) + (result.draftsCreated || 0),
        lastUsedTemplate: result.lastUsedTemplate || currentStats.lastUsedTemplate,
    };

    await saveBotStats(botId, newStats);
}

/**
 * Save bot statistics
 */
export async function saveBotStats(botId: string, stats: BotStats): Promise<void> {
    const supabase = createAdminClient();

    await supabase
        .from('bots')
        .update({ stats: stats as any })
        .eq('id', botId);
}

// Helpers

function mapRowToBot(row: any): EmailBot {
    return {
        id: row.id,
        userId: row.user_id,
        name: row.name,
        description: row.description,
        prompt: row.prompt,
        enabled: row.enabled ?? true, // Default to true if null/missing
        isPremium: row.is_premium ?? false,
        createdAt: new Date(row.created_at),
        updatedAt: new Date(row.updated_at),
        trigger: row.trigger || {},
        conditions: row.conditions || [],
        actions: row.actions || [],
        safety: row.safety || {},
        stats: row.stats || {
            totalExecutions: 0,
            successCount: 0,
            failureCount: 0,
            emailsSent: 0,
            draftsCreated: 0,
            lastUsedTemplate: undefined
        },
        acceptedTermsAt: row.accepted_terms_at ? new Date(row.accepted_terms_at) : undefined,
        acceptedTermsVersion: row.accepted_terms_version
    };
}

function mapBotToRow(bot: EmailBot): any {
    return {
        id: bot.id,
        user_id: bot.userId,
        name: bot.name,
        description: bot.description,
        prompt: bot.prompt || '',
        enabled: bot.enabled,
        is_premium: bot.isPremium,
        created_at: bot.createdAt,
        updated_at: bot.updatedAt,
        trigger: bot.trigger,
        conditions: bot.conditions,
        actions: bot.actions,
        safety: bot.safety,
        stats: bot.stats,
        accepted_terms_at: bot.acceptedTermsAt,
        accepted_terms_version: bot.acceptedTermsVersion
    };
}

